package fromScratch;

public class Modulus {
	public static void main(String args[]) {
		int x =2;
		double y =34.56;
		System.out.println(x%10);
		System.out.println(y%10);
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x/y);
		System.out.println(x%y);
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x+=x);
		
		
	}

}
